void test() {
    ll a, b, n;
    cin >> a >> b >> n;
    vector<ll> x(n);
    ll sum = 0;
    // finish initial b first, then start from end
    // can include atmost (a - 1) from any element
    for (auto &y : x)
        cin >> y, sum += min(a - 1, y);
    cout << sum + b << '\n';
}

