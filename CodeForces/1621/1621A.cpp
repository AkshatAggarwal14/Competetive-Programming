void Solution() {
    ll n, k;
    cin >> n >> k;
    vector<vector<char>> grid(n, vector<char>(n, '.'));
    for (ll i = 0; i < n && k > 0; i += 2) grid[i][i] = 'R', --k;
    if (k > 0) return void(cout << "-1\n");
    for (auto &x : grid) {
        for (auto &y : x) cout << y;
        cout << '\n';
    }
}

