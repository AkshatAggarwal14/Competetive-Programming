void Solution() {
    ll n;
    cin >> n;
    string s;
    cin >> s;
    ll ans = 0;
    for (ll i = 0; i < n; ++i) {
        if (i + 1 < n && s[i] == s[i + 1]) ++i;
        ++ans;
    }
    cout << ans << '\n';
}

