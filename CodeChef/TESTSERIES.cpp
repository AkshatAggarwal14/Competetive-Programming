using ll = int64_t;
#define all(x) (x).begin(), (x).end()
#define sz(x) (ll)(x).size()

void Solution() {
    vector<ll> r(5);
    for (ll i = 0; i < 5; ++i) cin >> r[i];
    ll c1 = count(all(r), 1);
    ll c2 = count(all(r), 2);
    if (c1 > c2)
        cout << "INDIA\n";
    else if (c1 == c2)
        cout << "DRAW\n";
    else
        cout << "ENGLAND\n";
}

