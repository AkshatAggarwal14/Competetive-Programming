void Solution() {
    ll n;
    cin >> n;
    if (n == 2) return void(cout << "-1\n");
    if (n & 1) {
        for (ll i = 1; i <= n; ++i) cout << i << ' ';
        cout << '\n';
    } else {
        for (ll i = n; i > 4; --i) cout << i << ' ';
        cout << "4 1 3 2\n";
    }
}

